import java.io.IOException;
import java.io.Reader;
import java.io.StringReader;

class Interpreter {
    
    public static void main(String[] args) {
        try {
            //String program = "let a := 2; b := map x to a * x; c := 5; in ((map x to if x < 3 then b else c)(a))(7)";
            //String program = "let a := c; b := a + 100; c := b + 100; in (map x to if x < 3 then b else c)(a)";
            //String program = "let a := c; b := a+1; c := b+2; in (map x to if x < 3 then b else c)(a)";
            String program = "let zeroes := cons(0,zeroes); in first(rest(zeroes))";
            Parser parser = new Parser(new StringReader(program));
            AST ast = parser.parse();
            Context.check(ast);
            Interpreter interp = new Interpreter(ast);
            System.out.println(interp.valueName());
            System.out.println("success");
        }
        catch (Exception e) {
            e.printStackTrace();
        }
    }

    /** Abstract Syntax Tree generated by the parser */
    private AST ast;

    /** Constructor */
    Interpreter(String fileName) throws IOException { this(new Parser(fileName)); }

    /** Constructor */
    Interpreter(Reader reader) { this(new Parser(reader)); }

    /** Constructor */
    Interpreter(Parser p) { this(p.parse()); }

    /** Constructor */
    Interpreter(AST ast) { this.ast = ast; }

    /** Interprets the program using call-by-value strategy */
    public JamVal callByValue() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_VALUE, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }

    /** Interprets the program using call-by-name strategy */
    public JamVal callByName() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NAME, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }

    /** Interprets the program using call-by-need strategy */
    public JamVal callByNeed() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NEED, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }
    
    public JamVal valueValue() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_VALUE, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }
    
    public JamVal nameValue() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NAME, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }
    public JamVal needValue() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NEED, EvaluationType.CALL_BY_VALUE)
                        )
                );
    }
    
    public JamVal valueName() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_VALUE, EvaluationType.CALL_BY_NAME)
                        )
                );
    }
    
    public JamVal nameName() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NAME, EvaluationType.CALL_BY_NAME)
                        )
                );
    }
    
    public JamVal needName() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NEED, EvaluationType.CALL_BY_NAME)
                        )
                );
    }
    
    
    public JamVal valueNeed() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_VALUE, EvaluationType.CALL_BY_NEED)
                        )
                );
    }
    
    public JamVal nameNeed() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NAME, EvaluationType.CALL_BY_NEED)
                        )
                );
    }
    
    public JamVal needNeed() {
        Context.check(ast);
        return this.ast.accept(
                new ASTInterpreter(
                        new Empty<Binding>(),
                        new EvaluationPolicy(EvaluationType.CALL_BY_NEED, EvaluationType.CALL_BY_NEED)
                        )
                );
    }
    
}


